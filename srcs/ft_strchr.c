#include "../includes/libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*str;

	str = (char *)s;
	i = 0;
	while (str[i] != '\0')
	{
		if (*str == (char) c)
			return (str);
		str++;
	}
	if (c == 0)
		return (str);
	return (0);
}
